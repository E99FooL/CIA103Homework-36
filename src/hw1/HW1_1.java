package HW1;
//• 請設計一隻Java程式,計算12,6這兩個數值的和與積
public class HW1_1 {
	public static void main(String[] args) {
        // 定義兩個整數變數
        int num1 = 12;
        int num2 = 6;
        // 計算和
        int sum = num1 + num2;
        // 計算積
        int product = num1 * num2;
        // 輸出結果
        System.out.println("兩個數的和是: " + sum);
        System.out.println("兩個數的積是: " + product);
    }
}

