package hw1;
public class HW1_4 {
	    // 定義常數 PI
private static final double PI = 3.1415;
public static void main(String[] args) {
	    // 圓的半徑
double radius = 5.0;
	    // 計算圓的面積
double area = PI * radius * radius;
	    // 計算圓的周長
double circumference = 2 * PI * radius;
	    // 輸出結果
	        System.out.println("圓的面積: " + area);
	        System.out.println("圓的周長: " + circumference);
	    }
	}

